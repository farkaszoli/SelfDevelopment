package egypercesek.folyo;

public class Palya
{
    private final int hosszusag;
    private final int szelesseg;

    public Palya(int hosszusag, int szelesseg) {
        this.hosszusag = hosszusag;
        this.szelesseg = szelesseg;
    }

    public int getHosszusag() {
        return hosszusag;
    }

    public int getSzelesseg() {
        return szelesseg;
    }

}

